package Weekly_Assignment;

public class Week3AssignmentQ1_MarksConversion {

	    public static void main(String[] args) {
	        // first Initial the float value
	        float marks = 87.6f;

	        // here Float to Double 
	        double doubleMarks = (double) marks;

	        // here Double to Int 
	        int intMarks = (int) doubleMarks;

	        // here Int to Char
	        
	        char charValue = (char) intMarks;

	        
	        System.out.println("Float value: " + marks);
	        System.out.println("Double value: " + doubleMarks);
	        System.out.println("Integer value: " + intMarks);
	        System.out.println("Character value: " + charValue);
	    }
	}


